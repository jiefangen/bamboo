package org.panda.business.admin.common.constant;

/**
 * 时间常量类
 *
 * @author fangen
 * @since JDK 11 2022/5/15
 */
public class TimeConstants {
    public static final long ONE_SECOND = 1000L; // 1秒
    public static final long ONE_MINUTE = 60 * ONE_SECOND; // 1分钟
    public static final long ONE_HOUR = 60 * ONE_MINUTE; // 1小时
    public static final long ONE_DAY = 24 * ONE_HOUR; // 1天
}
